from math import *
def add(x,y):
    c = x+y
    return c
def sub(x,y):
    c = x-y
    return c
def mult(x,y):
    c = x*y
    return c
def div(x,y):
    c = x/y
    return c
def root(x,y):
    c= sqrt(x)
    y = sqrt(y)
    return c,y
def square(x,y):
    c = x*x
    z = y * y
    return c,z
def  cube(x,y):
    c = x*x*x
    z = y*y*y
    return c,z